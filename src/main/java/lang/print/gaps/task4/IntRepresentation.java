package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char a='C';
        char b='H';
        char c='A';
        char d='R';
        int e=a;
        int f=b;
        int g=c;
        int h=e;
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
    }
}
